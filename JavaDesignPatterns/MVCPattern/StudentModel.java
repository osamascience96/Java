
public class StudentModel {
	private String rollNo;
	private String name;
	
	public void setRollNo(String rollNo) {
		this.rollNo = rollNo;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getRollNo() {
		return this.rollNo;
	}
	
	public String getName() {
		return this.name;
	}
}

