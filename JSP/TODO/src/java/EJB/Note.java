package EJB;

/**
 *
 * @author osama
 */
public class Note {
    private int id;
    private User user;
    private String data;
    private String createdAt;

    public Note() {
    }

    public Note(int id, User user, String data, String createdAt) {
        this.id = id;
        this.user = user;
        this.data = data;
        this.createdAt = createdAt;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public String getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(String createdAt) {
        this.createdAt = createdAt;
    }
    
    
}
